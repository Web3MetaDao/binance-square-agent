class PersonaBuilder:
    """
    通过交互式访谈提取用户人设，生成 persona.md。
    """

    def __init__(self):
        self.client = OpenAI()
        self.answers = {}

    def run_interview(self, quick_mode: bool = False):
        """
        运行访谈。
        quick_mode=True 时只问每个维度的前3题（适合快速测试）。
        """
        DATA_DIR.mkdir(parents=True, exist_ok=True)
        print("\n" + "═" * 55)
        print("  币安广场运营系统智能体 — 灵魂提取访谈")
        print("  本访谈将帮助智能体理解您的风格与认知，")
        print("  从而生成真正属于您的内容。")
        print("  输入 'skip' 可跳过某道题。")
        print("═" * 55 + "\n")

        total = 0
        for section, questions in INTERVIEW_QUESTIONS.items():
            print(f"\n── {section} ──")
            q_list = questions[:3] if quick_mode else questions
            for i, q in enumerate(q_list, 1):
                total += 1
                print(f"\nQ{total}. {q}")
                answer = input("您的回答: ").strip()
                if answer.lower() == "skip":
                    continue
                self.answers[q] = answer

        print(f"\n\n[灵魂提取] 访谈完成，共收集 {len(self.answers)} 条回答，正在生成人设文件...")
        self._generate_persona()

    def _generate_persona(self):
        """调用 LLM 将访谈记录提炼为结构化 persona.md。"""
        qa_text = "\n".join(
            [f"Q: {q}\nA: {a}" for q, a in self.answers.items()]
        )
        prompt = f"""以下是一位加密货币投资者的深度访谈记录：

{qa_text}

请根据以上访谈内容，生成一份结构化的"数字人设文档"（persona.md），包含以下六个模块：

## 1. 人格底色
（用2-3段话描述此人的性格特征、交易风格、核心信念）

## 2. 交易逻辑
（描述此人的市场判断框架、常用指标、决策流程）

## 3. 社交黑话库
（列出此人常用的加密圈词汇、口头禅、表达习惯，每条一行）

## 4. 情绪触发机制
（描述什么会让此人兴奋、恐惧、冲动，以及他如何应对）

## 5. 币安生态认知
（描述此人对币安广场、合约、BNB的看法和使用习惯）

## 6. 个人背景与愿景
（描述此人的背景、进圈经历、在广场上想建立的品牌形象）

要求：语言要真实、具体，像是在描述一个真实的人，不要泛泛而谈。"""

        try:
            response = self.client.chat.completions.create(
                model=LLM_MODEL,
                messages=[{"role": "user", "content": prompt}],
                max_tokens=2000,
                temperature=0.7,
            )
            persona_content = response.choices[0].message.content.strip()
            header = (
                f"# 数字人设文档（Persona）\n"
                f"生成时间：{datetime.now().strftime('%Y-%m-%d %H:%M:%S')}\n\n"
            )
            full_content = header + persona_content
            with open(PERSONA_FILE, "w") as f:
                f.write(full_content)
            print(f"[灵魂提取] ✅ persona.md 已生成：{PERSONA_FILE}")
        except Exception as e:
            print(f"[灵魂提取] LLM 生成失败，保存原始访谈记录: {e}")
            with open(PERSONA_FILE, "w") as f:
                f.write(f"# 原始访谈记录\n生成时间：{datetime.now().isoformat()}\n\n")
                for q, a in self.answers.items():
                    f.write(f"**Q:** {q}\n**A:** {a}\n\n")
